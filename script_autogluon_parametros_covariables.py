ts_data.head()



# âš™ï¸ 5. Definir y entrenar predictor
predictor = TimeSeriesPredictor(
    prediction_length=2,
    target='tn',
    freq='MS',  # Frecuencia mensual (Month Start)
    eval_metric ="MAPE" ,  # MÃ©trica de evaluaciÃ³n


)



predictor.fit(
    ts_data,
    num_val_windows=2,
    presets="best_quality",
    refit_full=True,

)

predictor.leaderboard(silent=True)

# ðŸ”® 6. Generar predicciÃ³n
forecast = predictor.predict(ts_data)

from pprint import pprint
info = predictor.get_model_info('WeightedEnsemble')
pprint(info['info']['weights'])  # pesos por modelo base

# Extraer predicciÃ³n media y filtrar febrero 2020
